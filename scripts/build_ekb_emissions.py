FIELDS = ["vsego_t", "tverdye_t", "so2_t", "co_t", "nox_t", "los_t", "prochie_t"]


def main():
    # Step 1: merge entries by name
    merged: dict[str, list[float]] = defaultdict(lambda: [0.0] * 7)
    for name, _oktmo, *values in raw_data:
        for i in range(7):
            merged[name][i] += values[i]

    # Step 2: build municipality list
    municipalities = []
    for name, vals in sorted(merged.items(), key=lambda x: -x[1][0]):
        mid = ID_MAP.get(name)
        if not mid:
            print(f"WARNING: no ID for '{name}', skipping")
            continue

        coords = COORDS.get(name)
        if not coords:
            print(f"WARNING: no coords for '{name}', skipping")
            continue

        vsego = round(vals[0])
        data_status = "report" if vsego >= 100 else "estimate"

        # Default main_sources
        sources = MAIN_SOURCES.get(name)
        if not sources:
            sources = "котельные, ЖКХ" if vsego < 100 else "теплоснабжение, малый бизнес"

        # Display name: use NAME_MAP or default to "<name> ГО"
        display_name = NAME_MAP.get(name)
        if not display_name:
            display_name = f"{name} ГО"

        mo_type = TYPE_MAP.get(name, "go")

        municipalities.append({
            "id": mid,
            "name": display_name,
            "short": SHORT_MAP.get(name, name),
            "type": mo_type,
            "lat": coords[0],
            "lon": coords[1],
            "vsego_t": vsego,
            "tverdye_t": round(vals[1]),
            "so2_t": round(vals[2]),
            "co_t": round(vals[3]),
            "nox_t": round(vals[4]),
            "los_t": round(vals[5]),
            "prochie_t": round(vals[6]),
            "data_status": data_status,
            "main_sources": sources,
        })

    total = sum(m["vsego_t"] for m in municipalities)
    biggest = max(municipalities, key=lambda m: m["vsego_t"])

    result = {
        "version": "1.0",
        "year": 2024,
        "form": "2-ТП Воздух",
        "source": "Росприроднадзор (rpn.gov.ru)",
        "published": "2025-03",
        "units": "тонны/год",
        "fields": ["vsego_t", "tverdye_t", "so2_t", "co_t", "nox_t", "los_t", "prochie_t"],
        "note": (
            f"Данные за 2024 г. Свердловская область, {len(municipalities)} МО. "
            f"Общий объём выбросов: {total:,} т/год. "
            f"Крупнейший: {biggest['short']} ({biggest['vsego_t']:,} т). "
            "Записи с одинаковым названием (ОКТМО 655xx + 657xx) объединены суммированием."
        ),
        "municipalities": municipalities,
    }

    out_path = pathlib.Path(__file__).resolve().parent.parent / "data" / "cities" / "ekaterinburg" / "emissions_2tp.json"
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(json.dumps(result, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"Wrote {len(municipalities)} municipalities to {out_path}")
    print(f"Total emissions: {total:,} т/год")
    print(f"Biggest emitter: {biggest['name']} — {biggest['vsego_t']:,} т")


if __name__ == "__main__":
    main()
